#include <iostream>
using namespace std;
int main() {
    int x;
    int y;

    cout << "Enter a number: ";
    cin >> x;
    cout << "Enter another number: ";
    cin >> y;
    cout << "The sum of two numbers are: " << x + y << endl;
    cout << "The difference of two numbers are: " << x - y << endl;
    cout << "The product of two numbers are: " << x * y << endl;
    cout << "The division of two numbers are: " << x / y << endl;
    cout << "The remainder of two numbers are: " << x % y << endl;



    return 0;
}